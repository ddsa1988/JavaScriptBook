"use strict";

const a = Array.of(); //Empty array with length of zero
console.log(a, a.length);
console.log();

const b = Array.of(1); //Array with a single numeric argument
console.log(b, b.length);
console.log();

const c = Array.of(0, 1, 2, 3, 4); //Array with five numeric arguments
console.log(c, c.length);
