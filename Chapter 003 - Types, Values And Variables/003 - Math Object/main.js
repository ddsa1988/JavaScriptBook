"use strict";

console.log("Math Object:");
console.log();

console.log(Math.pow(2, 5)); //2 to the power 53
console.log(Math.round(0.6), Math.round(0.4)); //Round 0.6 and 0.5 to the nearest integer
console.log(Math.ceil(0.6), Math.ceil(0.4)); //Round up 0.6 and 0.5 to an integer
console.log(Math.floor(0.6), Math.floor(0.4)); //Round down 0.6 and 0.5 to an integer
console.log(Math.abs(-10)); //Absolute value
console.log(Math.max(-10, 20, 100, 25)); //Return the largest value
console.log(Math.min(-10, -50, 100, 25)); //Return the smallest value
console.log(Math.random()); //Pseudo-random number between 0 and 1 (exclude 1)
console.log(Math.PI); //Pi
console.log(Math.E); //The base of the natural logarithm
console.log(Math.sqrt(9)); //Square root
console.log(Math.sin(45)); // Trigonometry sin
console.log(Math.log(100)); //Base 10 logarithm of 100
console.log(Math.exp(3));
console.log(Math.cbrt(27)); //Cube root
